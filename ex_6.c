#include <stdio.h>
int main()
{
    float pi = 3.1416;
    int radius = 6;
    float perimeter = 2 * pi * radius;
    float area = pi * radius * radius;
    printf("Perimeter of the circle is %f inches\n", perimeter);
    printf("Area of the circle is %f inches\n", area);
    
return 0;
}
