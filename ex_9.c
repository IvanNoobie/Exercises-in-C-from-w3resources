#include <stdio.h>
int main()
{
    int a;
    int b;
    int sum;
    printf("Print 1st integer: \n");
    scanf("%d", &a);
    printf("Print 2nd integer: \n");
    scanf("%d", &b);
    sum = a + b;
    printf("Sum of the above two integers is %d\n", sum);
    
return 0;
}
